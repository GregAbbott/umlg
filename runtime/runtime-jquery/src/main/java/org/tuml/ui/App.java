package org.tuml.ui;

public class App {
	private String rootUrl;
	private String uri;

	public String getUri() {
		return uri;
	}

	public App setUri(String uri) {
		this.uri = uri;
		return this;
	}

	public String getRootUrl() {
		return rootUrl;
	}

	public App setRootUrl(String rootUrl) {
		this.rootUrl = rootUrl;
		return this;
	}
	
}
